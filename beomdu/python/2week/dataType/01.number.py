# 01 숫자형
## 덧셈
print(4 + 7) # 정수 + 정수
print(4.0 + 7.0) # 소수 + 소수
print(4 + 7.0) # 정수 + 소수

## 뺼셈
print(2 - 4)
print(2.0 - 4.0)
print(2.0 - 4)

## 곱셈
print(5 * 3)
print(5.0 * 3.0)
print(5 * 3.0)

## 나머지
print(7 % 3)
print(7.0 % 3.0)
print(7.0 % 3)

## 거듭제곱
print(2 ** 3)
print(2.0 ** 3.0)
print(2 ** 3.0)

## 나누기
print(7 / 2)
print(6 / 2)
print(7.0 / 2)
print(6.0 / 2.0)